<a name="howItWorks"><b>1 - Como funciona o Classi-A?</b> <BR>
O Classi-A é um site que possui uma conta de usuário denominada gerenciador de anúncios. Nele você pode cadastrar, apagar ou adicionar anúncios. A quantidade máxima é de 20 anúncios. Os usuários poderão visualizar seus anúncios por categorias, seguidas de suas subcategorias e especificações, caso possuam. Ao ser visualizado um anúncio, suas informações de contato serão apresentadas, como telefones, e-mail ou ICQ, possibilitando uma melhor facilidade entre anunciante e usuário.<BR><BR>

<a name="theSignIn"><b>2 - Como é feito o cadastro?</b><BR>
Através do menu principal, clique em cadastro. Preencha o formulário corretamente. As instruções iniciais de acesso serão enviadas para o seu e-mail. Este email deve ser guardado com cuidado. Após ler suas instruções entre na página principal do Classi-A e digite sua senha e nome de usuário na tabela "login".<BR><BR>

<a name="announcesManagement"><b>3 - Como funciona a minha conta (gerenciador dos anúncios)?</b><BR>
Seu gerenciador de anúncios possui a relação de todos seus anúncios numa tabela. Esta tabela possui o título do anúncio e duas opções a respeito do mesmo; exclusão e modificação. Clicando no título do anúncio você visualizará todas suas informações. Caso algo esteja incorreto, você pode modificar clicando no ícone indicado. Se você quiser apagar, clique em excluir.
Você pode cadastrar um novo anúncio, dependendo do número disponível em sua conta; ou modificar seus dados pessoais.<BR><BR>

<a name="siteNavigation"><b>4 - Como é feita a navegação no site?</b><BR>
A relação das categorias dos anúncios está na página principal. Escolhendo uma delas, você visualizará todos os anúncios pertencentes àquela categoria. Caso queira especificar ainda mais sua procura por anúncios, você pode especificar o tipo de anúncio (venda, compra...) ou escolher uma subcategoria daquela categoria, caso exista. Assim você poderá encontrar um anúncio mais rapidamente. Ao achar um anúncio que lhe interesse, apenas clique sobre o título que todas as informações daquele anúncio serão mostradas.
  O deslocamento pelas categorias ou subcategorias também podem ser feitas pelos diretórios mostrados no topo da página.<BR><BR>

<a name="searchTool"><b>5 - Existe uma ferramenta de busca de anúncios?</b><BR>
Caso queira pesquisar, o Classi-A lhe disponibiliza uma ferramenta de pesquisa. Digitando uma palavra chave, será listado todos anúncios cujos os títulos contenham aquela palavra chave. As palavras chaves devem ser bastante simples e direta. <BR>
Ex: Nokia, Fiat, Pastor Alemão, entre outros.<BR>
Palavras chaves com mais de 20 caracteres serão ignorados.<BR><BR>

<a name="theLast5Announces"><b>6 - Meu anúncio aparece na lista dos últimos 5 anúncios?</b><BR>
Ao cadastrar seu anúncio em sua gerência de anúncios, automaticamente o mesmo aparecerá na lista, exceto se seu anúncio pertencer a massagens, por motivos de controle.<BR><BR>

<a name="whyRecommend"><b>7 - Por que recomendar o Classi-A?</b><BR>
Para que seus amigos e familiares possam conhecer seus anúncios, aumentando assim as chances de você fechar negócio.<BR><BR>

<a name="forgottenPassword"><b>8 - Esqueci minha senha e não tenho mais meu email de cadastro. Como faço para receber meus dados de acesso?</b><BR>
O email que você recebe depois de efetuado o cadastro é muito importante. É nele que você recebe as primeiras informações, juntamente com seu nome de usuário e senha. Estes devem ser guardados com segurança pelo dono da conta. Caso venha a perder este email e não se lembre de sua senha, digite seu nome de usuário na tabela "esqueci minha senha". Automaticamente seu nome de usuário e senha serão enviados para seu email.<BR><BR>

<a name="emailIndication"><b>9 - Recebi um email com a indicação de um anúncio pertencente ao Classi-A. Como faço para visualizar esse anúncio?</b><BR>
Basta escrever o código que está em seu email na tabela "ver anúncio". Lembre que o código deve ser digitado da mesma maneira que está em seu email, ou seja, difere letras maiúsculas e minúsculas. O melhor a fazer é copiar o código e colar no campo descrito acima.<BR><BR>

<a name="moreAnnounces"><b>10 - Quero colocar mais de 20 anúncios em minha conta.</b><BR>
Se você quiser ter um maior número de anúncios, você pode se tornar nosso cliente especial. Indicado para quem quer vender seus produtos aqui no Classi-A. <BR><BR>

<a name="specialUser"><b>11 - Como faço para se tornar um cliente especial?</b><BR>
Entre em contato com business@classi-a.com.br e informe seus dados e e\necessidade.<BR><BR>

<a name="announceInfo"><b>12 - Quais as informações de cadastro de anúncio?</b><BR>
Seu anúncio deve ter um título sugestivo. É por ele que nossa ferramenta de busca pesquisa por anúncios. Uma categoria e se existir uma subcategoria e especificação do anúncio. Uma breve descrição com no máximo 250 caracteres e opcionalmente uma imagem do anúncio.<BR><BR>

<a name="personalInformation"><b>13 - Minhas informações pessoais serão mostradas? </b><BR>
Não. Apenas informações de contato serão mostradas quando um de seus anúncios for visualizado por algum usuário. Suas informações serão apenas para controle, segurança e pesquisa do site. 

